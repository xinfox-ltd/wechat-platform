declare(strict_types=1);

namespace XinFox\WechatPlatform;

class Util
{
    /**
     * @param $length
     * @return string
     */
    public static function randomStr($length): string
    {
        // 字符组合
        $str = 'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
        $len = strlen($str) - 1;
        $randStr = '';
        for ($i = 0; $i < $length; $i++) {
            $num = mt_rand(0, $len);
            $randStr .= $str[$num];
        }

        return $randStr;
    }
}